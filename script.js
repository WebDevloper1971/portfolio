const themeToggler = document.getElementById("theme-toggler");
const gridContainer = document.getElementById("grid-container");
const techGridContainer = document.getElementById("tech-grid");

function navigate(id) {
  const element = document.getElementById(id);
  if (element) {
    element.scrollIntoView({ behavior: "smooth" });
  }
}

function createCard(name, image, link, description, linkSVG) {
  const card = document.createElement("li");
  card.classList.add("project-card");

  const projectImage = document.createElement("img");
  projectImage.src = image;
  projectImage.classList.add("project-image");

  const overlay = document.createElement("div");
  overlay.classList.add("overlay");

  const linkSection = document.createElement("div");
  linkSection.classList.add("project-link-section");

  const anchor = document.createElement("a");
  anchor.href = link;
  anchor.target = "_blank";

  const linkImage = document.createElement("img");
  linkImage.src = linkSVG;

  anchor.appendChild(linkImage);

  const projectName = document.createElement("h2");
  projectName.textContent = name;

  linkSection.appendChild(projectName);
  linkSection.appendChild(anchor);

  const projectDescription = document.createElement("p");
  projectDescription.textContent = description;

  overlay.appendChild(linkSection);
  overlay.appendChild(projectDescription);

  card.appendChild(projectImage);
  card.appendChild(overlay);

  return card;
}

function techLogo(id, image, link) {
  const card = document.createElement("li");
  card.classList.add("tech-card");

  const anchor = document.createElement("a");
  anchor.href = link;

  const projectImage = document.createElement("img");
  projectImage.src = image;

  card.appendChild(anchor);
  anchor.appendChild(projectImage);

  return card;
}

